Map::Map()
{	
	pamant = Texturi::incaracaTextura("Poze/pamant.png");
	apa = Texturi::incaracaTextura("Poze/apa.png");
	iarba = Texturi::incaracaTextura("Poze/iarba.png");

	incarcaMap(lvl1);
	src.x = src.y = 0 ;
	src.w = dst.w = 32;
	dst.h = src.h = 32;
	dst.x = dst.y = 0;
	
}

void Map::incarcaMap(int vect[20][25]) 
{
	for (int i = 0; i < 20; ++i)
	{
		for (int j = 0; j < 25; ++j)
		{
			harta[i][j] = vect[i][j];
		}
	}

}

void Map::deseneazaMap() 
{

	int tip = 0;

	for (int i = 0; i < 20; ++i)
	{
		for (int j = 0; j < 25; ++j)
		{
			tip = harta[i][j];
			dst.x = j*32;
			dst.y = i*32;

			switch (tip)
			{
			case 0:
				Texturi::desen(apa, src, dst);
				break;
			case 1:
				Texturi::desen(pamant, src, dst);
				break;
			case 2:
				Texturi::desen(iarba, src, dst);
				break;
			default:
				break;
			}
		}
	}
}